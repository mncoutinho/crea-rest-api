  CREATE OR REPLACE FUNCTION "CORPORATIVO"."DISTRIBUICAO_IMPORTACAO_SIACOL" 
( ID_DEPARTAMENTO IN NUMBER) 
        RETURN NUMBER 
    IS
        BOOLEAN NUMBER;
        CURSOR CMOVIMENTOS IS
        --SELECT * FROM PRT_MOVIMENTOS WHERE DESPACHADO = 0 AND FK_ID_DESTINO_DEPARTAMENTOS = ID_DEPARTAMENTO AND FK_ID_SITUACOES <> 3 AND TO_CHAR(DATAENVIO,'YYYY') >=  TO_CHAR(SYSDATE-365,'YYYY');
        --SELECT PM.* FROM PRT_MOVIMENTOS PM, PRT_PROTOCOLOS P WHERE   P.ID = PM.FK_ID_PROTOCOLOS AND P.ULTIMOMOVIMENTO = PM.ID AND PM.FK_ID_DESTINO_DEPARTAMENTOS = ID_DEPARTAMENTO;
         SELECT PM.* FROM PRT_MOVIMENTOS PM, PRT_PROTOCOLOS P 
            WHERE   P.ID = PM.FK_ID_PROTOCOLOS 
                AND P.ULTIMOMOVIMENTO = PM.ID 
                AND PM.FK_ID_DESTINO_DEPARTAMENTOS = 1205
                AND P.FK_ID_ASSUNTOS IN ( SELECT ID FROM PRT_ASSUNTOS WHERE REMOVIDO = 0 AND ID = P.FK_ID_ASSUNTOS)
                AND ROWNUM < 11
                ORDER BY PM.FK_ID_PROTOCOLOS DESC ;
        V_NAO_EXISTE NUMBER;
        V_ERRO NUMBER;
        V_PROTOCOLO PRT_PROTOCOLOS%ROWTYPE;
        V_SIACOL_PROTOCOLO SIACOL_PROTOCOLOS%ROWTYPE;
        V_INTERESSADO CAD_PESSOAS%ROWTYPE;
        V_NOME SIACOL_PROTOCOLOS.DS_INTERESSADO%TYPE;
        V_ASSUNTO PRT_ASSUNTOS%ROWTYPE;
        V_IDRESPONSAVEL SIACOL_HABILIDADE_PESSOA.FK_PESSOA%TYPE;
        V_RESPONSAVEL SIACOL_PROTOCOLOS.DS_RESPONSAVEL%TYPE;
        V_SITUACAOPROTOCOLO SIACOL_PROTOCOLOS.FK_SITUACAO_PROTOCOLO%TYPE;
        V_IDPESSOA CAD_FUNCIONARIOS.FK_ID_PESSOAS_FISICAS%TYPE;
BEGIN
        FOR VMOVI IN CMOVIMENTOS LOOP
        
                V_ERRO := 0;
                V_NAO_EXISTE:= 0;

                -- Verifica se já existe um protocolo dentro do SIACOL
                BEGIN
                 V_NAO_EXISTE := 1;
                 SELECT * INTO V_SIACOL_PROTOCOLO FROM SIACOL_PROTOCOLOS WHERE NO_PROTOCOLO = VMOVI.FK_ID_PROTOCOLOS;                  
                EXCEPTION
                  WHEN NO_DATA_FOUND THEN
                  V_NAO_EXISTE := 0;     
                  WHEN OTHERS THEN
                  V_ERRO:= 1;
                  V_NAO_EXISTE := 0; 
                END;
        
                IF V_NAO_EXISTE <> 1 THEN   
                    --DEVOLVE O PROTOCOLO SE FOR DO SIACOL E ESTIVER VOLTANDO PARA O MESMO DEPARTAMENTO( MUDANÇA DE STATUS )
                    IF V_SIACOL_PROTOCOLO.FK_DEPARTAMENTO = VMOVI.FK_ID_DESTINO_DEPARTAMENTOS THEN           
                        UPDATE SIACOL_PROTOCOLOS
                          SET FK_SITUACAO_PROTOCOLO = 2
                        WHERE ID = V_SIACOL_PROTOCOLO.ID;
                        V_ERRO:= 1; 
                    END IF;
                 END IF;
                --SE NAO FOR DO SIACOL OU SE FOI ENCAMINHADO PARA OUTRO DEPARTAMENTO ELE FAZ A DISTRIBUIÇÃO        
                IF V_ERRO = 0 AND V_NAO_EXISTE = 0 THEN 
        
                   -- PEGA O PROTOCOLO, INTERESSADO E ASSUNTO PARA POPULAR FK E DS;
                    SELECT * INTO V_PROTOCOLO FROM PRT_PROTOCOLOS WHERE NUMERO = VMOVI.FK_ID_PROTOCOLOS;
                    BEGIN
                        SELECT * INTO V_INTERESSADO FROM CAD_PESSOAS WHERE CODIGO = V_PROTOCOLO.FK_ID_PESSOAS;
                        EXCEPTION
                          WHEN NO_DATA_FOUND THEN
                          SELECT * INTO V_INTERESSADO FROM CAD_PESSOAS WHERE CODIGO = 0;
                          WHEN TOO_MANY_ROWS THEN
                          SELECT * INTO V_INTERESSADO FROM CAD_PESSOAS WHERE CODIGO = 0; 
                          WHEN OTHERS THEN
                          SELECT * INTO V_INTERESSADO FROM CAD_PESSOAS WHERE CODIGO = 0;
                    END;   
                    
                    
                    --SELECT * INTO V_ASSUNTO FROM PRT_ASSUNTOS WHERE CODIGO = V_PROTOCOLO.FK_ID_ASSUNTOS;
        
                    --PEGA A DESCRICAO DO INTERESSADO CONFORME O TIPO PESSOA;
                    begin
                        SELECT * INTO V_NOME FROM (
                            SELECT NOME FROM CAD_PESSOAS_FISICAS WHERE CODIGO = V_PROTOCOLO.FK_ID_PESSOAS
                                UNION 
                            SELECT DESCRICAO FROM CAD_RAZOES_SOCIAIS WHERE FK_CODIGO_PJS = V_PROTOCOLO.FK_ID_PESSOAS AND ATIVO = 1 AND FK_CODIGO_PJS is not null
                        ) TMP;
                        EXCEPTION
                          WHEN NO_DATA_FOUND THEN
                          V_NOME := 'NÃO INFORMADO'; 
                          WHEN TOO_MANY_ROWS THEN
                          V_NOME := 'NÃO INFORMADO'; 
                          WHEN OTHERS THEN
                          V_NOME := 'NÃO INFORMADO'; 
                    END;                    
                    
                    V_IDRESPONSAVEL := 0;
                    V_IDPESSOA := 0; 
                    V_SITUACAOPROTOCOLO := 17;
                    
                    -- Não insere nenhuma pessoa responsavel, esses responsáveis serão atualizados manualmente
                    SELECT NOME INTO V_RESPONSAVEL FROM CAD_PESSOAS_FISICAS WHERE CODIGO = V_IDPESSOA;                  
        
                    --INSERI O PROTOCOLO NA TABELA SIACOL_PROTOCOLO;
                    INSERT INTO SIACOL_PROTOCOLOS 
                        (   ID,
                            NO_PROTOCOLO,
                            NO_PROCESSO,
                            FK_SITUACAO_PROTOCOLO,
                            FK_DEPARTAMENTO,
                            OBSERVACAO,
                            FK_ASSUNTO,
                            DT_CADASTRO,
                            FK_INTERESSADO,
                            FK_ASSUNTO_SIACOL,
                            DS_INTERESSADO,
                            DS_ASSUNTO,
                            FK_RESPONSAVEL,
                            DS_RESPONSAVEL,
                            DT_SIACOL, 
                            DS_TIPO_ASSUNTO,
                            IMPORTADO, 
                            ATIVO,
                            DIGITALIZADO) 
                    SELECT  SQ_SIACOL_PROTOCOLOS.NEXTVAL,
                            V_PROTOCOLO.NUMERO,
                            V_PROTOCOLO.PROCESSO,
                            V_SITUACAOPROTOCOLO,
                            VMOVI.FK_ID_DESTINO_DEPARTAMENTOS,
                            NULL,
                            V_PROTOCOLO.FK_ID_ASSUNTOS,
                            V_PROTOCOLO.DATAEMISSAO,
                            V_PROTOCOLO.FK_ID_PESSOAS,
                            NULL,
                            V_NOME,
                            V_ASSUNTO.DESCRICAO,
                            V_IDRESPONSAVEL,
                            V_RESPONSAVEL, 
                            SYSDATE, 
                            V_ASSUNTO.TIPOASSUNTO, 
                            1, 
                            1,
                            V_PROTOCOLO.POSSUI_DIGITALIZACAO
                            FROM DUAL;
                END IF;
        END LOOP;
    RETURN 0;
END ;

