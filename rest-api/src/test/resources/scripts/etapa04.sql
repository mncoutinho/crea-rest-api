--  DDL for Function GETRESPONSAVELSIACOL
--------------------------------------------------------

  CREATE OR REPLACE FUNCTION "CORPORATIVO"."GETRESPONSAVELSIACOL" 
( ID_DEPARTAMENTO IN NUMBER , ID_ASSUNTO IN NUMBER ) 
        RETURN NUMBER 
    IS
        ID_RESPONSAVEL NUMBER;
BEGIN
    begin
            SELECT FK_PESSOA INTO ID_RESPONSAVEL FROM (
            SELECT FK_PESSOA, NVL(TB1.QTD,0) QTD FROM SIACOL_HABILIDADE_PESSOA,
                ( SELECT FK_RESPONSAVEL, COUNT(*) QTD FROM SIACOL_PROTOCOLOS 
                    WHERE FK_ASSUNTO = ID_ASSUNTO
                    AND FK_DEPARTAMENTO = ID_DEPARTAMENTO
                    AND TO_CHAR(DT_SIACOL,'DD/MM/YY') = TO_CHAR(SYSDATE,'DD/MM/YY')  
                    GROUP BY FK_RESPONSAVEL )TB1 
                WHERE TB1.FK_RESPONSAVEL(+) = FK_PESSOA  
                    AND FK_ASSUNTO = ID_ASSUNTO
                    AND FK_DEPARTAMENTO = ID_DEPARTAMENTO
                GROUP BY FK_PESSOA, NVL(TB1.QTD,0), TB1.QTD, 0, TB1.FK_RESPONSAVEL 
                ORDER BY QTD ASC
            ) TB2 WHERE ROWNUM <= 1;
            exception
            when no_data_found then
                RETURN 0;
            when others then
                RETURN 0;
    end;
        RETURN ID_RESPONSAVEL;

END ;

