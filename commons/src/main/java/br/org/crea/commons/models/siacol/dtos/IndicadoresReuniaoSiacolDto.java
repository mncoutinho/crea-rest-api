package br.org.crea.commons.models.siacol.dtos;

public class IndicadoresReuniaoSiacolDto {
	
	private int qtdPresentes;
	
	private int qtdAusentes;
	
	private int qtdLicenciados;
	
	private int qtdEmpossados;

	public int getQtdPresentes() {
		return qtdPresentes;
	}

	public void setQtdPresentes(int qtdPresentes) {
		this.qtdPresentes = qtdPresentes;
	}

	public int getQtdAusentes() {
		return qtdAusentes;
	}

	public void setQtdAusentes(int qtdAusentes) {
		this.qtdAusentes = qtdAusentes;
	}

	public int getQtdLicenciados() {
		return qtdLicenciados;
	}

	public void setQtdLicenciados(int qtdLicenciados) {
		this.qtdLicenciados = qtdLicenciados;
	}

	public int getQtdEmpossados() {
		return qtdEmpossados;
	}

	public void setQtdEmpossados(int qtdEmpossados) {
		this.qtdEmpossados = qtdEmpossados;
	}

}
