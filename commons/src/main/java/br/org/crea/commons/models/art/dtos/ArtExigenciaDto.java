package br.org.crea.commons.models.art.dtos;

public class ArtExigenciaDto {
	
	private String codigo;
	
	private String descricao;
	
	private String conteudoGerador;
	
	private String statusTipoAcao;
	
	private String data;
	
	private String descricaoAnalise;
	
	private String situacaoLiberacao;
	
	private String dataDaLiberacao;
	
	private String descricaoLiberacao;

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getConteudoGerador() {
		return conteudoGerador;
	}

	public void setConteudoGerador(String conteudoGerador) {
		this.conteudoGerador = conteudoGerador;
	}

	public String getStatusTipoAcao() {
		return statusTipoAcao;
	}

	public void setStatusTipoAcao(String statusTipoAcao) {
		this.statusTipoAcao = statusTipoAcao;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getDescricaoAnalise() {
		return descricaoAnalise;
	}

	public void setDescricaoAnalise(String descricaoAnalise) {
		this.descricaoAnalise = descricaoAnalise;
	}

	public String getSituacaoLiberacao() {
		return situacaoLiberacao;
	}

	public void setSituacaoLiberacao(String situacaoLiberacao) {
		this.situacaoLiberacao = situacaoLiberacao;
	}

	public String getDataDaLiberacao() {
		return dataDaLiberacao;
	}

	public void setDataDaLiberacao(String dataDaLiberacao) {
		this.dataDaLiberacao = dataDaLiberacao;
	}

	public String getDescricaoLiberacao() {
		return descricaoLiberacao;
	}

	public void setDescricaoLiberacao(String descricaoLiberacao) {
		this.descricaoLiberacao = descricaoLiberacao;
	}
	
	

}
